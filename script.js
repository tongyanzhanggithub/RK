const selected = new Set();

let currentLanguage = localStorage.getItem("roadlink-language") || "en";

function t(key) {
  return translations[currentLanguage]?.[key] || translations.en[key] || key;
}

function productText(product) {
  return product.i18n[currentLanguage] || product.i18n.en;
}

function tagText(tag) {
  return tagTranslations[currentLanguage]?.[tag] || tag;
}

function applyLanguage(language) {
  currentLanguage = translations[language] ? language : "en";
  localStorage.setItem("roadlink-language", currentLanguage);
  document.documentElement.lang = currentLanguage;
  languageSelect.value = currentLanguage;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.placeholder = t(node.dataset.i18nPlaceholder);
  });

  renderProducts(document.querySelector(".filter.active").dataset.filter);
  renderRfq();
}

function renderProducts(filter = "all") {
  grid.innerHTML = "";

  products
    .filter((product) => filter === "all" || product.category.includes(filter))
    .forEach((product) => {
      const [name, desc] = productText(product);
      const card = document.createElement("article");
      card.className = "product-card";
      card.innerHTML = `
        <div class="product-icon" aria-hidden="true">${product.icon}</div>
        <h3>${name}</h3>
        <p>${desc}</p>
        <div class="product-meta">${product.tags.map((tag) => `<span>${tagText(tag)}</span>`).join("")}</div>
        <button class="add-rfq ${selected.has(product.id) ? "selected" : ""}" type="button" data-product="${product.id}">
          ${selected.has(product.id) ? t("rfq.added") : t("rfq.add")}
        </button>
      `;
      grid.appendChild(card);
    });
}

function renderRfq() {
  rfqCount.textContent = selected.size;

  if (!selected.size) {
    rfqList.innerHTML = `<div class="rfq-item"><span>${t("rfq.empty")}</span></div>`;
    return;
  }

  rfqList.innerHTML = [...selected]
    .map((id) => {
      const product = products.find((item) => item.id === id);
      const [name] = productText(product);
      return `
        <div class="rfq-item">
          <span>${name}</span>
          <button type="button" aria-label="${t("rfq.remove")} ${name}" data-remove="${id}">${t("rfq.remove")}</button>
        </div>
      `;
    })
    .join("");
}

